original_string = 'PyThOn Is AwEsOmE'

#Applying . Lower()
lowercase = original_string.lower()
print('lower:',lowercase)

#Applying . Upper
uppercase = original_string.upper()
print('upper:',uppercase)

# Applying .capitalize()
capitalized = original_string.capitalize()
print('capitalize:',capitalized)

# Applying .title()
swapcased = original_string.swapcase()
print('swapcase:',swapcased)

# Applying .casefold()
casefolded = original_string.casefold()
print('casefold:',casefolded)






























